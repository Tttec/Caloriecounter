package com.example.caloriecounter;

public class Besin {
    private String name;
    private Integer protein;
    private Integer karbonhidrat;
    private Integer yag;
    private Integer kalori;

    public Besin() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getProtein() {
        return protein;
    }

    public void setProtein(Integer protein) {
        this.protein = protein;
    }

    public Integer getKarbonhidrat() {
        return karbonhidrat;
    }

    public void setKarbonhidrat(Integer karbonhidrat) {
        this.karbonhidrat = karbonhidrat;
    }

    public Integer getYag() {
        return yag;
    }

    public void setYag(Integer yag) {
        this.yag = yag;
    }

    public Integer getKalori() {
        return kalori;
    }

    public void setKalori(Integer kalori) {
        this.kalori = kalori;
    }
}
